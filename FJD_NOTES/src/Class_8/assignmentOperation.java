package Class_8;

public class assignmentOperation {
    public static void main(String[] args) {
        System.out.println("Assignment operation in Java");
        int x = 5;
       // x = x + 5;//x = 5 + 5 = 10
        x += 5; // Here we assign the 5 value
        // use the operator like +, -, * , / = equal after that give the value
        System.out.println(x);

        int y = 10;
        //y = y * 20;//y = 10 * 20 = 200
        y *= 20; // use the operator like +, -, * , / = equal after that give the value
        System.out.println(y);
    }
}
