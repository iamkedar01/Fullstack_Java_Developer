package Class_8;

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println("Arithmetic Operation in Java");
        int  a = 10 ;
        int b = 20;

        System.out.println("a = "+ a +" b = "+b +" sum = "+ (a + b));
        System.out.println("a = "+ a +" b = "+b +" sub = "+ (a - b));
        System.out.println("a = "+ a +" b = "+b +" mul = "+ (a * b));
        System.out.println("a = "+ a +" b = "+b +" div = "+ (a / b));
        System.out.println("a = "+ a +" b = "+b +" rem = "+ (a % b));

    }
}
